package lk.ijse.moderntech.model;

/**
 *
 * @author Shamal
 */
public class EmployeeModel {
    private String employeeID;
    private String name;
    private String contactNo;
    private String email;
    private String Address;
    private String role;

    public EmployeeModel() {
    }

    public EmployeeModel(String employeeID, String name, String contactNo, String email, String Address, String role) {
        this.employeeID = employeeID;
        this.name = name;
        this.contactNo = contactNo;
        this.email = email;
        this.Address = Address;
        this.role = role;
    }

    /**
     * @return the employeeID
     */
    public String getEmployeeID() {
        return employeeID;
    }

    /**
     * @param employeeID the employeeID to set
     */
    public void setEmployeeID(String employeeID) {
        this.employeeID = employeeID;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the contactNo
     */
    public String getContactNo() {
        return contactNo;
    }

    /**
     * @param contactNo the contactNo to set
     */
    public void setContactNo(String contactNo) {
        this.contactNo = contactNo;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the Address
     */
    public String getAddress() {
        return Address;
    }

    /**
     * @param Address the Address to set
     */
    public void setAddress(String Address) {
        this.Address = Address;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    @Override
    public String toString() {
        return "EmployeeModel{" + "employeeID=" + employeeID + ", name=" + name + ", contactNo=" + contactNo + ", email=" + email + ", Address=" + Address + ", role=" + role + '}';
    }
    
}
