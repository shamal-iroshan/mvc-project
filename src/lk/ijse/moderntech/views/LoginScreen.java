    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        UserModel user = new UserModel(
                txtUsername.getText(),
                txtPassword.getText()
        );
        try {
            boolean isCorrect = new LoginController().checkLogin(user);
            if (isCorrect) {
                String empID = new LoginController().getEmpID(user);
                new Main(empID).setVisible(true);
                this.dispose();
            } else {
                new InformationBox(new javax.swing.JFrame(), true, "Check user name and password").setVisible(true);
            }
        } catch (ClassNotFoundException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Class not found exception").setVisible(true);
        } catch (SQLException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Sql exception").setVisible(true);
        } catch (NoSuchAlgorithmException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "No such algorythm exception.").setVisible(true);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

