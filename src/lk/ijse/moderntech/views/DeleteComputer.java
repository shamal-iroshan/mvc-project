    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (txtComputerid.getText().trim().length() > 0) {
            try {
                boolean isDeleted = new DeleteComputerController().deleteComputer(txtComputerid.getText());
                if (isDeleted) {
                    new InformationBox(new javax.swing.JFrame(), true, "Deleted successfully.").setVisible(true);
                } else {
                    new InformationBox(new javax.swing.JFrame(), true, "Something went wrong, Please try again.").setVisible(true);
                }
            } catch (ClassNotFoundException ex) {
                new ErrorBox(new javax.swing.JFrame(), true, "Class not found exception").setVisible(true);
            } catch (SQLException ex) {
                new ErrorBox(new javax.swing.JFrame(), true, "Sql exception").setVisible(true);
            }
        }
        loadTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


