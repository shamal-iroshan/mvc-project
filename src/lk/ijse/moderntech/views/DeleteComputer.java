    private void loadTable() {
        try {
            List<ComputerModel> list = new DeleteComputerController().getAllComputers();
            DefaultTableModel model = (DefaultTableModel) tblComputer.getModel();
            model.setRowCount(0);

            for (ComputerModel computerModel : list) {
                model.addRow(new Object[]{
                    computerModel.getComputerID(),
                    computerModel.getWarrantyID(),
                    computerModel.getType(),
                    computerModel.getBrand(),
                    computerModel.getDescription()
                });
            }
        } catch (ClassNotFoundException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Class not found exception").setVisible(true);
        } catch (SQLException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Sql exception").setVisible(true);
        }
    }

