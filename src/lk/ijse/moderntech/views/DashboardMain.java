    private void tblLowstockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLowstockMouseClicked
        try {
            int row = tblLowstock.getSelectedRow();
            txtStockid.setText(tblLowstock.getValueAt(row, 0).toString());
            txtItemid.setText(tblLowstock.getValueAt(row, 1).toString());

            if (txtItemid.getText().substring(0, 1).equals("P")) {
                txtItemdetail.setText(new DashBoardController().getPartDetail(txtItemid.getText()));
            } else if (txtItemid.getText().substring(0, 1).equals("C")) {
                txtItemdetail.setText(new DashBoardController().getComputerDetail(txtItemid.getText()));
            }
            SupplierModel supplier = new DashBoardController().getSupplierDetail(txtStockid.getText());
            txtSuppliername.setText(supplier.getName());
            txtContactno.setText(supplier.getContactNo());
            
        } catch (ClassNotFoundException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Class not found exception").setVisible(true);
        } catch (SQLException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Sql exception").setVisible(true);
        }
    }//GEN-LAST:event_tblLowstockMouseClicked

