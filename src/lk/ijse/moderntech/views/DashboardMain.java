    private void loadLowStock() {
        try {
            List<StockModel> list = new DashBoardController().getLowStock();
            DefaultTableModel model = (DefaultTableModel) tblLowstock.getModel();
            model.setRowCount(0);

            for (StockModel stockModel : list) {
                String itemID = null;
                if (stockModel.getComputerID() != null) {
                    itemID = stockModel.getComputerID();
                } else if (stockModel.getPartID() != null) {
                    itemID = stockModel.getPartID();
                }
                model.addRow(new Object[]{
                    stockModel.getStockID(),
                    itemID,
                    stockModel.getQTYonhand()
                });
            }

        } catch (ClassNotFoundException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Class not found exception").setVisible(true);
        } catch (SQLException ex) {
            new ErrorBox(new javax.swing.JFrame(), true, "Sql exception").setVisible(true);
        }
    }

    private void clock() {
        Thread clk = new Thread() {
            public void run() {
                try {
                    for (;;) {
                        Calendar c1 = new GregorianCalendar();
                        int day = c1.get(Calendar.DAY_OF_MONTH);
                        int month = c1.get(Calendar.MONTH);
                        int year = c1.get(Calendar.YEAR);

                        int second = c1.get(Calendar.SECOND);
                        int min = c1.get(Calendar.MINUTE);
                        int hour = c1.get(Calendar.HOUR);
                        int am_pm = c1.get(Calendar.AM_PM);

                        String a_p = null;
                        if (am_pm == 1) {
                            a_p = "PM";
                        } else {
                            a_p = "AM";
                        }
                        lblTime.setText(hour + ":" + min + ":" + second + " " + a_p);
                        lblDate.setText(day + ":" + month + ":" + year);
                        sleep(1000);
                    }
                } catch (Exception ex) {
                    new ErrorBox(new javax.swing.JFrame(), true, "Error " + ex).setVisible(true);
                }
            }
        };
        clk.start();
    }
